# Sparator / Penghubung sep=
# Deklarasi variabel beserta nilainya
w, x, y, z = 10, 15, 20, 25

# Menampilkan tiap hasil variable
print(w, x, y, z)

# Penulisan pengunaan sparator
print(w, x, y, z, sep=' ')
print(w, x, y, z, sep=',')
print(w, x, y, z, sep=';')
print(w, x, y, z, sep=':')
print(w, x, y, z, sep='----')
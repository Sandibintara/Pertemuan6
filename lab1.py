# Penggunaan sparator / Penghubung({sep=})
w, x, y, z = 10, 15, 20, 25

# Penulisan tanpa sparator / penghubung
print(w, x, y, z)

# Pengunaan sparator
print(w, x, y, z, sep=' ')
print(w, x, y, z, sep=',')
print(w, x, y, z, sep=';')
print(w, x, y, z, sep=':')
print(w, x, y, z, sep='----')